#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
		while ((*s != '\0') && (*s != c))
	{
		s++;
	}
	if (*s == c || c == 0)
	{
		return ((char *)s);
	}
	return ((char*)NULL);
}
